    private void IncluiPesquisa() {

        int linha = jTblConsulta_Multi.getSelectedRow();

        String id;
        String sql;

        switch (var_consulta) {
            case 1:
                jTxtId.setText(jTblConsulta_Multi.getValueAt(linha, 0).toString());

            case 11:

                id = jTxtId.getText();
                sql = "SELECT * FROM cad_prazos WHERE id = " + id;
                cn.conecta();
                cn.executeConsulta(sql);
                try {
                    while (cn.rs.next()) {

                        String dts = dateIn.format(cn.rs.getObject("dt_solicitacao"));
                        String dtv = dateIn.format(cn.rs.getObject("dt_vencimento"));

                        jTxtIdObrigação.setText(cn.rs.getString("id_obrigacao"));
                        jTxtIdEstabelecimento.setText(cn.rs.getString("id_estabelecimento"));
                        jTxtProtocolo.setText(cn.rs.getString("protocolo"));
                        jTxtObs.setText(cn.rs.getString("obs"));
                        jSpSolicitacao.setValue(dateIn.parse(dts));
                        jSpVencimento.setValue(dateIn.parse(dtv));
                    }
                } catch (Exception ex) {

                    JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
                }
                cn.desconecta();

                if (!"".equals(jTxtIdEstabelecimento.getText())
                        && !"".equals(jTxtIdObrigação.getText())) {

                    var_consulta = 22;
                    IncluiPesquisa();
                    var_consulta = 33;
                    IncluiPesquisa();
                }

                break;

            case 2:

                jTxtIdObrigação.setText(jTblConsulta_Multi.getValueAt(linha, 0).toString());

            case 22:
                id = jTxtIdObrigação.getText();
                sql = "SELECT * FROM cad_prazos_tipos WHERE id = " + id;

                cn.conecta();
                cn.executeConsulta(sql);
                try {
                    while (cn.rs.next()) {
                        jTxtNomeObrigacao.setText(cn.rs.getString("descricao"));
                    }
                } catch (SQLException ex) {

                    JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
                }
                cn.desconecta();

                break;

            case 3:
                jTxtIdEstabelecimento.setText(jTblConsulta_Multi.getValueAt(linha, 0).toString());
            case 33:
                id = jTxtIdEstabelecimento.getText();
                sql = "SELECT a.id, a.razao_social, b.nome, a.inscricao_federal "
                        + "FROM cad_estabelecimentos a "
                        + "LEFT JOIN cad_empresas b on (b.id = a.id_empresa) "
                        + "WHERE a.id = " + id;

                cn.conecta();
                cn.executeConsulta(sql);
                try {
                    while (cn.rs.next()) {
                        jTxtNomeEmpresa.setText(cn.rs.getString("razao_social")
                                + " (" + cn.rs.getString("nome") + ")");
                    }
                } catch (SQLException ex) {

                    JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
                }
                cn.desconecta();
                break;

            case 4:

                jTxtId1.setText(jTblConsulta_Multi.getValueAt(linha, 0).toString());

            case 44:
                id = jTxtId1.getText();
                sql = "SELECT * FROM cad_prazos WHERE id = " + id;
                cn.conecta();
                cn.executeConsulta(sql);
                try {
                    while (cn.rs.next()) {

                        jTxtIdObrigação.setText(cn.rs.getString("id_obrigacao"));
                        jTxtIdEstabelecimento.setText(cn.rs.getString("id_estabelecimento"));
                    }
                } catch (Exception ex) {

                    JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
                }
                cn.desconecta();

                if (!"".equals(jTxtIdEstabelecimento.getText())
                        && !"".equals(jTxtIdObrigação.getText())) {

                    var_consulta = 22;
                    IncluiPesquisa();
                    var_consulta = 33;
                    IncluiPesquisa();
                }

        }
    }

    private void montaLista() {
        DefaultTableModel lista = (DefaultTableModel) jTblConsulta_Multi.getModel();
        jTxtPesquisa_Multi.grabFocus();

        lista.setColumnCount(0);
        lista.setRowCount(0);

        String sql;
        String id;
        String condicao;

        switch (var_consulta) {
            case 1:

                lista.addColumn("Id");
                lista.addColumn("Obrigação");
                lista.addColumn("Protocolo");
                lista.addColumn("Empresa");
                jTblConsulta_Multi.getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getColumnModel().getColumn(0).setMinWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMinWidth(60);

                if (!"".equals(jTxtPesquisa_Multi.getText())) {
                    condicao = "WHERE UPPER(c.razao_social) "
                            + "LIKE '%" + jTxtPesquisa_Multi.getText().toUpperCase() + "%' "
                            + "OR UPPER(a.protocolo) "
                            + "LIKE '%" + jTxtPesquisa_Multi.getText().toUpperCase() + "%' ";
                } else {
                    condicao = " ";
                }

                sql = "SELECT a.id, b.descricao as obrigacao, a.protocolo, c.razao_social "
                        + "FROM cad_prazos A "
                        + "LEFT JOIN cad_prazos_tipos B on (B.id = A.id_obrigacao) "
                        + "LEFT JOIN cad_estabelecimentos C on (C.id = A.id_estabelecimento)"
                        + condicao
                        + " ORDER BY c.razao_social, b.descricao, a.protocolo;";

                cn.conecta();
                cn.executeConsulta(sql);
                try {
                    while (cn.rs.next()) {

                        lista.addRow(new String[]{
                            cn.rs.getString("id"),
                            cn.rs.getString("obrigacao"),
                            cn.rs.getString("protocolo"),
                            cn.rs.getString("razao_social")
                        });
                    }
                } catch (SQLException ex) {

                    JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
                }
                cn.desconecta();
                break;

            case 2:

                lista.addColumn("Id");
                lista.addColumn("Descrição");

                jTblConsulta_Multi.getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getColumnModel().getColumn(0).setMinWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMinWidth(60);

                if (!"".equals(jTxtPesquisa_Multi.getText())) {
                    condicao = "WHERE UPPER(descricao) "
                            + "LIKE '%" + jTxtPesquisa_Multi.getText().toUpperCase() + "%'";
                } else {
                    condicao = "";
                }

                sql = "SELECT * FROM cad_prazos_tipos "
                        + condicao
                        + " ORDER BY descricao";

                cn.conecta();
                cn.executeConsulta(sql);
                try {
                    while (cn.rs.next()) {

                        lista.addRow(new String[]{
                            cn.rs.getString("id"),
                            cn.rs.getString("descricao"),});
                    }
                } catch (SQLException ex) {

                    JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
                }

                cn.desconecta();
                break;

            case 3:
                lista.addColumn("Id");
                lista.addColumn("Estabelecimento");
                lista.addColumn("Razão Social");

                jTblConsulta_Multi.getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getColumnModel().getColumn(0).setMinWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMinWidth(60);

                if (!"".equals(jTxtPesquisa_Multi.getText())) {
                    condicao = "WHERE UPPER(razao_social) "
                            + "LIKE '%" + jTxtPesquisa_Multi.getText().toUpperCase() + "%'";
                } else {
                    condicao = "";
                }

                sql = "SELECT a.id, a.razao_social, b.nome, a.inscricao_federal "
                        + "FROM cad_estabelecimentos a "
                        + "LEFT JOIN cad_empresas b on (b.id = a.id_empresa) "
                        + condicao
                        + " ORDER BY razao_social";

                cn.conecta();
                cn.executeConsulta(sql);
                try {
                    while (cn.rs.next()) {

                        lista.addRow(new String[]{
                            cn.rs.getString("id"),
                            cn.rs.getString("razao_social"),
                            cn.rs.getString("nome")
                        });
                    }
                } catch (SQLException ex) {

                    JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
                }

                cn.desconecta();

            case 4:
                var_consulta = 1;
                montaLista();
                var_consulta = 4;

        }
    }
