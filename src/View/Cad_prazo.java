    private void jBtnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGravarActionPerformed
        String id = jTxtId.getText();
        String obrigacao = jTxtIdObrigação.getText();
        String empresa = jTxtIdEstabelecimento.getText();
        String obs = jTxtObs.getText();
        String protocolo = jTxtProtocolo.getText();
        String dt_sol = dateOut.format(jSpSolicitacao.getValue());
        String dt_venc = dateOut.format(jSpVencimento.getValue());
        int usuario = TelaInicial.usuariosys;

        String sql;

        cn.conecta();
        if ("Cancelar".equals(jBtnIncluir.getText())) {
            cn.executeConsulta("show table status like 'cad_prazos';");
            try {
                while (cn.rs.next()) {
                    id = cn.rs.getString("auto_increment");
                }
                cn.rs.close();
            } catch (SQLException ex) {
                ConexaoMySQL.resultadoUpd = 2;
                JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
            }

            sql = "INSERT INTO cad_prazos (id_obrigacao, id_estabelecimento, "
                    + "protocolo, dt_solicitacao, dt_vencimento, obs, usu_inc, dt_inc) "
                    + "VALUES (" + obrigacao + "," + empresa + ",'" + protocolo + "',"
                    + "'" + dt_sol + "','" + dt_venc + "','" + obs + "'," + usuario + ", current_date());";
        } else {
            sql = "UPDATE cad_prazos set id_obrigacao = " + obrigacao + ", "
                    + "id_estabelecimento = " + empresa + ", "
                    + "protocolo = '" + protocolo + "', dt_solicitacao = '" + dt_sol + "', "
                    + "dt_vencimento = '" + dt_venc + "', obs = '" + obs + "', "
                    + "usu_alt = " + usuario + ", dt_alt = current_date() "
                    + "WHERE id = " + id + "; ";
        }
        cn.executeAtualizacao(sql);
        cn.desconecta();

        if (ConexaoMySQL.resultadoUpd < 1) {
            desabilitarEdicao();
            jTxtId.setText(id);
        }
    }//GEN-LAST:event_jBtnGravarActionPerformed

