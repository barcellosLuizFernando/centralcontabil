    private void montaTabela() {
        DefaultTableModel lista = (DefaultTableModel) jTable1.getModel();

        lista.setColumnCount(0);
        lista.setRowCount(0);

        lista.addColumn("Código");
        lista.addColumn("Produtor");
        lista.addColumn("Processo");
        lista.addColumn("Início do Processo");
        lista.addColumn("Análise");
        lista.addColumn("Crédito");

        jTable1.setDefaultRenderer(Object.class, new ColorRender());

        String sql = "SELECT a.id,a.id_produtor,a.id_estabelecimento,"
                + "a.dt_inicio_processo,round(sum(b.valor_credito),1) as credito,"
                + "c.nome as produtor,concat(repeat('0',9-length(d.inscri_est)),d.inscri_est) as inscri_est,"
                + "concat(repeat('0',11-length(c.inscricao_federal)),c.inscricao_federal) as cpf,"
                + "a.credito_liberado,a.deferido,a.dt_analise,a.dt_inicial,"
                + "a.dt_final,a.informacao,a.percentual_aproveitado,a.processo,"
                + "a.vendas FROM controladoria.produtor_icms a "
                + "LEFT JOIN controladoria.produtor_icms_notas b on (b.id = a.id) "
                + "LEFT JOIN controladoria.cad_pessoas c on (c.id = a.id_produtor) "
                + "LEFT JOIN controladoria.icms_estabelecimentos d on (d.id = a.id_estabelecimento) "
                + "WHERE a.deferido = 1 "
                + "group by a.id, a.processo "
                + "ORDER BY c.nome; ";

        if (cn.conecta()) {
            try {
                cn.executeConsulta(sql);
                while (cn.rs.next()) {
                    lista.addRow(new String[]{
                        cn.rs.getString("id"),
                        cn.rs.getString("produtor"),
                        cn.rs.getString("processo"),
                        dateIn.format(cn.rs.getDate("dt_inicio_processo")),
                        dateIn.format(cn.rs.getDate("dt_analise")),
                        df.format(cn.rs.getDouble("credito_liberado"))
                    });
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                cn.desconecta();
            }
        }
    }
