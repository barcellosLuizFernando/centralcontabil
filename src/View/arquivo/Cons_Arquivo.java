    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String assunto = jCbAssuntos.getSelectedItem().toString();
        String data = dateOut.format(jSpData.getValue());
        String estante;

        String sql = "SELECT DISTINCT sequencia,nome_assunto,nome_empresa,"
                + "nome_estabelecimento,nome_tipo,nome_local,estante,prateleira "
                + "FROM rel_arquivo_pastas "
                + "WHERE nome_assunto ='" + assunto + "' "
                + "AND data_inicial <= '" + data + "' "
                + "AND data_final >= '" + data + "'";

        DefaultTableModel pastas = (DefaultTableModel) jTblPastas.getModel();
        pastas.setColumnCount(0);
        pastas.setRowCount(0);

        //pastas.addColumn("Empresa");
        pastas.addColumn("Unidade");
        pastas.addColumn("Caixa");
        //pastas.addColumn("Assunto");
        pastas.addColumn("Tipo Arquivo");
        pastas.addColumn("Local");

        jTblPastas.getColumnModel().getColumn(1).setMaxWidth(50);
        jTblPastas.getColumnModel().getColumn(1).setMinWidth(50);
        jTblPastas.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(50);
        jTblPastas.getTableHeader().getColumnModel().getColumn(1).setMinWidth(50);

        cn.conecta();
        cn.executeConsulta(sql);

        try {
            while (cn.rs.next()) {

                if ((cn.rs.getString("estante") != null)
                        && (cn.rs.getString("prateleira")) != null) {
                    estante = ", E: " + cn.rs.getString("estante") + ", P: " + cn.rs.getString("prateleira");
                } else {
                    estante = "";
                }

                pastas.addRow(new String[]{
                    //cn.rs.getString(12),
                    cn.rs.getString(4),
                    cn.rs.getString(1),
                    //cn.rs.getString(6),
                    cn.rs.getString(5),
                    cn.rs.getString("nome_local") + estante
                });
            }
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
        }

        cn.desconecta();
    }//GEN-LAST:event_jButton1ActionPerformed


