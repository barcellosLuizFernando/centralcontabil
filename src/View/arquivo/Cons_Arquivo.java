    public void listaAssuntos() {
        String sql = "SELECT * FROM cad_arquivo_assuntos "
                + "order by descricao";

        cn.conecta();
        cn.executeConsulta(sql);
        try {
            while (cn.rs.next()) {
                jCbAssuntos.addItem(cn.rs.getString(2));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Cons_Arquivo.class.getName()).log(Level.SEVERE, null, ex);
        }
        cn.desconecta();
    }

