    public void DefineSequencial() {

        String empresa = jTxtIdEmpresa.getText();
        String departamento = jTxtIdDepartamento.getText();

        if (!"".equals(empresa) && !"".equals(departamento)) {

            try {

                String sql = "SELECT max(sequencia) as id FROM cad_arquivo_pastas "
                        + "WHERE ID_EMPRESA = " + empresa
                        + " AND ID_DEPARTAMENTO = " + departamento;

                cn.conecta();
                cn.executeConsulta(sql);

                while (cn.rs.next()) {

                    //ID ATUAL INICIA EM ZERO. SE A CONSULTA RETORNA NULL, ASSUME 1
                    int id_atual;
                    id_atual = cn.rs.getInt("id");
                    id_atual++;

                    prox_arquivo = "" + id_atual;

                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Não foi possível determinar o número sequencial. Será assumido seq = 1");

            }

            cn.desconecta();

            jTxtSequencia.setText(prox_arquivo);
        }

    }

    public void AdicionaAssunto() {
        if (!"".equals(jTxtNomeAssunto.getText())) {
            upd_assuntos = 0;

            if (!"0".equals(jTxtIdCapa.getText())) {

                for (int i = 0; i < jTblAssuntos.getRowCount(); i++) {
                    String NRO1 = jTxtIdCapa.getText();
                    String NRO2 = jTblAssuntos.getValueAt(i, 4).toString();
                    if (NRO2.equals(NRO1)) {
                        JOptionPane.showMessageDialog(rootPane, "Esta capa já foi incluida!", null, JOptionPane.INFORMATION_MESSAGE);
                        DefaultTableModel modelr = (DefaultTableModel) jTblAssuntos.getModel();
                        modelr.removeRow(i);

                    }
                }
            }

            String dti = dateIn.format(jTxtDataInicial.getValue());
            String dtf = dateIn.format(jTxtDataFinal.getValue());

            DefaultTableModel modelr = (DefaultTableModel) jTblAssuntos.getModel();
            modelr.addRow(new String[]{
                jTxtIdAssunto.getText(),
                jTxtNomeAssunto.getText(),
                dti,
                dtf,
                jTxtIdCapa.getText(),
                jTxtObs.getText()});

            jTxtIdCapa.setText("");
            jTxtNomeAssuntoCapa.setText("");
            jTxtIdAssunto.setText("");
            jTxtNomeAssunto.setText("");
            jTxtObs.setText("");

            jBtnRemoveAssunto.setEnabled(true);

        } else {
            JOptionPane.showMessageDialog(rootPane, "Você precisa digitar os dados.");
        }
    }

