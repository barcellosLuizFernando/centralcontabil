                    //DEVOLVE DATA PARA O JSPINNER, PARA SER CAPTURADA
                    jTxtDataInicial.setValue(dateIn.parse(jTblAssuntos.getValueAt(x, 2).toString()));
                    jTxtDataFinal.setValue(dateIn.parse(jTblAssuntos.getValueAt(x, 3).toString()));

                    String id_assunto = String.valueOf(jTblAssuntos.getValueAt(x, 0));
                    String nome_assunto = String.valueOf(jTblAssuntos.getValueAt(x, 1));
                    String dti = dateOut.format(jTxtDataInicial.getValue());
