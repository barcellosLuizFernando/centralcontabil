    private void jTxtIdCapaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTxtIdCapaFocusLost
        int etiqueta = 0;

        String id_capa = jTxtIdCapa.getText();
        jTxtNomeAssuntoCapa.setText("");
        jTxtIdAssunto.setText("");
        jTxtNomeAssunto.setText("");

        String sqlcp = "SELECT * FROM rel_arquivo_pastas A "
                + "WHERE a.id_capa = '" + id_capa + "';";

        cn.conecta();
        cn.executeConsulta(sqlcp);

        try {
            while (cn.rs.next()) {
                etiqueta = cn.rs.getInt("id_etiqueta");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }

        cn.desconecta();

        if (etiqueta > 0) {
            JOptionPane.showMessageDialog(this, "Este assunto já está cadastrado na etiqueta " + etiqueta);
            jTxtIdCapa.setText("");
        } else {

            //jTxtIdCapa.setText("");
            String id_empresa = jTxtIdEmpresa.getText();
            String id_departamento = jTxtIdDepartamento.getText();

            String sql = "SELECT * FROM CAD_ARQUIVO_CAPAS B "
                    + "left join rel_arquivo_pastas A on (b.id = a.id_capa) "
                    + "where a.id_capa is null "
                    + " AND b.ID =" + id_capa
                    + " and b.id_empresa =" + id_empresa;

            if (!"".equals(jTxtIdEmpresa.getText())
                    && !"".equals(jTxtIdCapa.getText())) {

                cn.conecta();
                cn.executeConsulta(sql);

                try {
                    while (cn.rs.next()) {
                        String dti = dateIn.format(cn.rs.getObject(8));
                        String dtf = dateIn.format(cn.rs.getObject(9));

                        jTxtNomeAssuntoCapa.setText(cn.rs.getString(7));
                        jTxtIdAssunto.setText(cn.rs.getString(6));
                        jTxtNomeAssunto.setText(cn.rs.getString(7));
                        jTxtDataInicial.setValue(dateIn.parse(dti));
                        jTxtDataFinal.setValue(dateIn.parse(dtf));
                    }
                } catch (SQLException | ParseException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
                cn.desconecta();
            } else {
                jTxtNomeAssunto.setText("");
                jTxtNomeAssuntoCapa.setText("");
                jTxtIdAssunto.setText("");
            }

            if (!"".equals(jTxtNomeAssuntoCapa.getText())) {
                jBtnPesquisaAssunto.setEnabled(false);
            } else {
                jBtnPesquisaAssunto.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jTxtIdCapaFocusLost

