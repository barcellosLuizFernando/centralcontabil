    private void jBtnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGravarActionPerformed
        String id = jTxtId.getText();
        String descricao = jTxtDescricao.getText().toUpperCase();
        String prateleira;
        
        if (jCheckBox1.isSelected()) {
            prateleira = "S";
        } else {
            prateleira = "N";
        }
        
        cn.conecta();
        
        if (!"".equals(descricao)
                && var_gravar == 1) {
            try {
                String sql = "INSERT INTO CAD_ARQUIVO_LOCAIS (descricao, prateleiras)"
                        + "VALUES ('" + descricao + "','" + prateleira + "')";
                
                cn.executeAtualizacao(sql);
                
                DesabilitarEdicao();
                limpaCampos();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Não foi possível gravar no banco de dados!");
            }
        } else if (!"".equals(descricao)
                && var_gravar == 2) {
            try {
                
                String sql = "UPDATE CAD_ARQUIVO_LOCAIS SET descricao = '" + descricao + "', "
                        + "prateleiras = '" + prateleira + "' "
                        + "WHERE ID = " + jTxtId.getText();
                cn.executeAtualizacao(sql);
                
                DesabilitarEdicao();
                limpaCampos();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Não foi possível gravar no banco de dados!");
            }
            
        }
        
        cn.desconecta();

    }//GEN-LAST:event_jBtnGravarActionPerformed

