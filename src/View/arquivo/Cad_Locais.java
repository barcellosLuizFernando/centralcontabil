    private void jBtnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDeletarActionPerformed
        int resposta;
        
        String descricao = jTxtDescricao.getText();
        
        resposta = JOptionPane.showConfirmDialog(this, "Você deseja realmente excluir o local "
                + descricao + "?", null, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        cn.conecta();
        switch (resposta) {
            
            case 0:
                
                String id = jTxtId.getText();
                
                String sql = "DELETE FROM cad_arquivo_locais "
                        + "WHERE id = " + id;
                String sql2 = "DELETE FROM cad_arquivo_locais_detalhe "
                        + "WHERE id = " + id;
                
                cn.executeAtualizacao(sql);
                cn.executeAtualizacao(sql2);
                
                break;
            default:
                JOptionPane.showMessageDialog(this, "Nenhum registro foi excluido!");
                break;
        }
        
        switch (ConexaoMySQL.resultadoUpd) {
            
            case 0:
                JOptionPane.showMessageDialog(this, "Local " + descricao + " foi excluído!");
                limpaCampos();
                DesabilitarEdicao();
                break;
            default:
                JOptionPane.showMessageDialog(this, "Nenhum registro foi excluido!");
                
                break;
        }
        cn.desconecta();
    }//GEN-LAST:event_jBtnDeletarActionPerformed

