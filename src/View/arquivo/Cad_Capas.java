    public void IncluiPesquisa() {
        int linha;

        switch (var_consulta) {
            case 1:
                //INCLUI CAPAS PELO BOTAO

                String sql2 = "SELECT * FROM cad_arquivo_capas "
                        + "where id = " + jTxtId.getText();

                cn.conecta();
                cn.executeConsulta(sql2);

                try {
                    while (cn.rs.next()) {
                        String dti = dateIn.format(cn.rs.getObject(8));
                        String dtf = dateIn.format(cn.rs.getObject(9));

                        jTxtIdEmpresa.setText(cn.rs.getString(2));
                        jTxtNomeEmpresa.setText(cn.rs.getString(3));
                        jTxtIdEstabelecimento.setText(cn.rs.getString(4));
                        jTxtNomeEstabelecimento.setText(cn.rs.getString(5));
                        jTxtIdAssunto.setText(cn.rs.getString(6));
                        jTxtNomeAssunto.setText(cn.rs.getString(7));
                        jTxtDataInicial.setValue(dateIn.parse(dti));
                        jTxtDataFinal.setValue(dateIn.parse(dtf));
                        JTxaObservacoes.setText(cn.rs.getString(10));
                    }
                } catch (SQLException | ParseException ex) {
                    JOptionPane.showMessageDialog(this, ex);
                }
                cn.desconecta();
                break;

            case 2:
                //INCLUI EMPRESA

                String sql = "SELECT a.id, b.id as id_empresa, b.nome "
                        + "FROM cad_estabelecimentos a "
                        + "LEFT JOIN cad_empresas b on (b.id = a.id_empresa) "
                        + "WHERE a.id =" + jTxtIdEstabelecimento.getText();

                cn.conecta();
                cn.executeConsulta(sql);

                try {
                    String id_empresa;
                    String nome_empresa;
                    while (cn.rs.next()) {

                        id_empresa = cn.rs.getString(2);
                        nome_empresa = cn.rs.getString(3);

                        jTxtIdEmpresa.setText(id_empresa);
                        jTxtNomeEmpresa.setText(nome_empresa);
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, ex);
                }

                break;
            case 3:
                //INCLUI ESTABELECIMENTO
                linha = jTblConsulta_Multi.getSelectedRow();

                jTxtIdEstabelecimento.setText(jTblConsulta_Multi.getValueAt(linha, 0).toString());
                jTxtNomeEstabelecimento.setText(jTblConsulta_Multi.getValueAt(linha, 1).toString());

                var_consulta = 2;

                IncluiPesquisa();

                break;
            case 4:
                //INCLUI ASSUNTO
                linha = jTblConsulta_Multi.getSelectedRow();

                jTxtIdAssunto.setText(jTblConsulta_Multi.getValueAt(linha, 0).toString());
                jTxtNomeAssunto.setText(jTblConsulta_Multi.getValueAt(linha, 1).toString());

                break;
        }
    }

