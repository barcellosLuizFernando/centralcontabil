    private void jBtnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGravarActionPerformed
        String id = jTxtId.getText();
        String id_empresa = jTxtIdEmpresa.getText();
        String nome_empresa = jTxtNomeEmpresa.getText();
        String id_estabelecimento = jTxtIdEstabelecimento.getText();
        String nome_estabelecimento = jTxtNomeEstabelecimento.getText();
        String id_assunto = jTxtIdAssunto.getText();
        String nome_assunto = jTxtNomeAssunto.getText();
        String dt_ini = dateOut.format(jTxtDataInicial.getValue());
        String dt_fim = dateOut.format(jTxtDataFinal.getValue());
        String obs = JTxaObservacoes.getText();
        int usuario = TelaInicial.usuariosys;

        if (var_gravar == 1
                && !"".equals(jTxtIdAssunto.getText())
                && !"".equals(jTxtIdEmpresa.getText())
                && !"".equals(jTxtIdEstabelecimento.getText())
                && !"".equals(jTxtDataInicial.getValue())
                && !"".equals(jTxtDataFinal.getValue())) {
            String sql = "INSERT INTO cad_arquivo_capas (id_empresa, nome_empresa, "
                    + "id_estabelecimento, nome_estabelecimento, id_assunto, "
                    + "nome_assunto, dt_inicial, dt_final, observacao, dt_inclusao, usu_inc) "
                    + "VALUES ('" + id_empresa + "', '" + nome_empresa + "', '" + id_estabelecimento
                    + "', '" + nome_estabelecimento + "', '" + id_assunto + "', '" + nome_assunto
                    + "', '" + dt_ini + "', '" + dt_fim + "', '" + obs + "', current_date(), " + usuario + ")";
            cn.conecta();
            try {
                cn.executeAtualizacao(sql);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
                cn.desconecta();
            }

            cn.desconecta();
            ImprimeCadastro();

        } else if (var_gravar == 2
                && !"".equals(jTxtIdAssunto.getText())
                && !"".equals(jTxtIdEmpresa.getText())
                && !"".equals(jTxtIdEstabelecimento.getText())
                && !"".equals(jTxtDataInicial.getValue())
                && !"".equals(jTxtDataFinal.getValue())) {

            String sql = "UPDATE cad_arquivo_capas set id_empresa =" + id_empresa
                    + ", nome_empresa = '" + nome_empresa + "', id_estabelecimento =" + id_estabelecimento
                    + ", nome_estabelecimento = '" + nome_estabelecimento + "', id_assunto =" + id_assunto
                    + ", nome_assunto = '" + nome_assunto + "', dt_inicial = '" + dt_ini + "'"
                    + ", dt_final = '" + dt_fim + "', observacao = '" + obs + "' "
                    + ", usu_alt = " + usuario + " "
                    + "where id = " + id;
            cn.conecta();
            try {
                cn.executeAtualizacao(sql);
                //JOptionPane.showMessageDialog(this, "Executou o comando update!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
            cn.desconecta();
            ImprimeCadastro();
        } else {
            JOptionPane.showMessageDialog(null, "Algum campo não está preenchido!");
        }
    }//GEN-LAST:event_jBtnGravarActionPerformed

