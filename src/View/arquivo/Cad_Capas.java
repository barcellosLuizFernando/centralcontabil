        jTxtId.setText("NOVO");

        try {

            String sql = "SELECT max(id) as id FROM cad_arquivo_capas ";

            cn.conecta();
            cn.executeConsulta(sql);

            while (cn.rs.next()) {

                int id_atual;
                id_atual = cn.rs.getInt("id");
                id_atual++;

                prox_capa = "" + id_atual;

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Não foi possível determinar o número do registro. Será assumido id = 1");

        }
        cn.desconecta();


