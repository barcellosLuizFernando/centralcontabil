    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int idUser = TelaInicial.usuariosys;
        
        String senha1 = jPasswordField1.getText();
        String senha2 = jPasswordField2.getText();
        
        if (!senha1.equals(senha2)){
            JOptionPane.showMessageDialog(this, "As senhas não são idênticas.");
        } else {
            cn.conecta();
            cn.executeAtualizacao("UPDATE cad_usuarios SET pwd = '" + senha1 + "', "
                    + "dt_alteracao = current_date() WHERE id = '" + idUser + "';");
            cn.desconecta();
            
            if(ConexaoMySQL.resultadoUpd < 1){
                JOptionPane.showMessageDialog(this, "Senha alterada com sucesso!");
                this.dispose();
            }
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


