    private void jBtnAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAcessarActionPerformed

        String user = jTxtUsuario.getText();
        String pwd = jPasswordField.getText();
        int id = 0;
        String pwdbd = null;
        int result = 0;

        cn.conecta();
        cn.executeConsulta("SELECT nome,pwd,id from cad_usuarios where login = '" + user + "';");
        try {
            while (cn.rs.next()) {
                pwdbd = cn.rs.getString(2);
                user = cn.rs.getString(1);
                id = cn.rs.getInt(3);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível verificar o login e a senha!");
        }
        cn.desconecta();

        if (pwd.equals(pwdbd)) {
            result = 1;
        }

        switch (result) {

            default:

            if (jTxtUsuario.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Informe o Usuário!");
                jTxtUsuario.grabFocus();
            } else if (pwd.equals(pwdbd)) {
                
                TelaInicial.recebendo(user, id);
                
                //new TelaInicial().controlaAcesso();
                
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Usuário incorreto!!!");
                jTxtUsuario.grabFocus();
            }

            break;

            case 0:
            JOptionPane.showMessageDialog(null, "Usuário ou senha não conferem!");
            break;
        }
    }//GEN-LAST:event_jBtnAcessarActionPerformed

