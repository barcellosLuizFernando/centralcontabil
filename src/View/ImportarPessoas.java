    private void buscaPessoa() {
        jTextField2.setText("");

        String sql;

        switch (var_consulta) {
            case 1:
                String id = jTextField1.getText();
                jTextField2.setText("");
                
                fb.conecta();

                sql = "SELECT a.nome,a.cpf_cnpj,b.nome as cidade,b.uf,"
                        + "a.endereco,a.nro_endereco,a.complemento,a.bairro,"
                        + "a.cep from cad_terceiros a "
                        + "left join cad_municipios b on (b.codigo = a.municipio) "
                        + "WHERE a.cpf_cnpj = '" + id + "';";

                fb.executeConsulta(sql);

                try {
                    while (fb.rs.next()) {
                        nome = fb.rs.getString("nome");
                        inscricao_federal = fb.rs.getString("cpf_cnpj");
                        uf = fb.rs.getString("uf");
                        cidade = fb.rs.getString("cidade");
                        endereco = fb.rs.getString("endereco");
                        nro = fb.rs.getString("nro_endereco");
                        complemento = fb.rs.getString("complemento");
                        bairro = fb.rs.getString("bairro");
                        cep = fb.rs.getString("cep");

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(ImportarPessoas.class.getName()).log(Level.SEVERE, null, ex);
                }

                fb.desconecta();

                jTextField2.setText(nome);

                //JOptionPane.showMessageDialog(this, endereco);

                break;

            case 2:

                try {

                    cn.conecta();

                    sql = "INSERT INTO cad_pessoas (nome,inscricao_federal,uf, "
                            + "cidade,endereco,nro,complemento,bairro,cep) "
                            + "VALUES ( '" + nome + "','" + inscricao_federal + "',"
                            + "'" + uf + "','" + cidade + "','" + endereco + "',"
                            + "'" + nro + "','" + complemento + "','" + bairro + "',"
                            + "'" + cep + "')";

                    cn.executeAtualizacao(sql);
                    //JOptionPane.showMessageDialog(this, sql);

                    if (ConexaoMySQL.resultadoUpd < 1) {

                        JOptionPane.showMessageDialog(this, "Importação realizada com sucesso.");

                        jTextField1.setText("");
                        jTextField2.setText("");
                    }
                    cn.desconecta();

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e);
                }

                break;

        }
    }
