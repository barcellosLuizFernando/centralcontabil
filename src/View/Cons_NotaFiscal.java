    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel lista = (DefaultTableModel) jTable1.getModel();

        String nf = jTextField1.getText();
        String unidade = jCbUnidades.getSelectedItem().toString();
        String cliente = jCBEmpresas.getSelectedItem().toString();

        String condicao1;
        String condicao2;
        String condicao3;

        String sql;

        if (!"".equals(nf)) {
            condicao1 = " AND numero_nf = '" + nf + "' ";
        } else {
            condicao1 = " ";
        }

        if (!"TODAS".equals(unidade)) {
            condicao2 = " AND cnpj_proprio = '" + unidade + "' ";
        } else {
            condicao2 = " ";
        }

        if (!"TODAS".equals(cliente)) {
            condicao3 = " AND nome_terceiro = '" + cliente + "' ";
        } else {
            condicao3 = " ";
        }

        sql = "SELECT * FROM notas_disponiveis WHERE valor >= 0 "
                + condicao1
                + condicao2
                + condicao3
                + " ORDER BY nome_terceiro, cnpj_proprio, numero_nf";

        montaTabela();

        cn.conecta();
        cn.executeConsulta(sql);
        try {
            while (cn.rs.next()) {
                lista.addRow(new String[]{
                    cn.rs.getString("nome_terceiro"),
                    cn.rs.getString("cnpj_proprio"),
                    cn.rs.getString("numero_nf"),
                    cn.rs.getString("emissao"),
                    cn.rs.getString("quantidade3"),
                    cn.rs.getString("quantidade")
                });
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Não foi possível consultar o banco de dados.");
        }
        cn.desconecta();
        
    }//GEN-LAST:event_jButton1ActionPerformed


