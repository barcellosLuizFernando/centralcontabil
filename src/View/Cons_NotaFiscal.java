    private void montaListas() {

        jCBEmpresas.removeAllItems();
        jCbUnidades.removeAllItems();

        String sql;

        cn.conecta();
        // LISTA EMPRESAS
        sql = "select distinct a.nome_terceiro from cad_notas_fiscais a ORDER BY a.nome_terceiro";

        cn.executeConsulta(sql);
        try {
            jCBEmpresas.addItem("TODAS");
            while (cn.rs.next()) {
                jCBEmpresas.addItem(cn.rs.getString(1));
            }
            cn.rs.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível montar o relatório");
        }

        //CONSULTA TODAS AS UNIDADES
        sql = "select distinct a.cnpj_proprio from cad_notas_fiscais a ORDER BY a.cnpj_proprio;";

        cn.executeConsulta(sql);

        try {
            jCbUnidades.addItem("TODAS");
            while (cn.rs.next()) {
                jCbUnidades.addItem(cn.rs.getString(1));
            }
            cn.rs.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível montar o relatório");
        }
        cn.desconecta();
    }

