    private void gravar() {
        String idProd = jTxtIdProd.getText();
        String idArea = jTxtIdArea.getText();
        String inscrEst = jTxtIE.getText();
        String desc = jTxtDescricao.getText();
        String area = jTxtArea.getText().replace(".", "").replace(",", ".");
        String nirf = jTxtNirf.getText();
        String incra = jTxtIncra.getText();
        String matricula = jTxtMatricula.getText();
        String endereco = jTxtEndereco.getText();
        String numero = jTxtNumero.getText();
        String bairro = jTxtBairro.getText();
        String municipio = jCbMunicipio.getSelectedItem().toString();
        String atividades = jTxtAtividades.getText();

        if ("".equals(jTxtIdArea.getText())) {
            sql = "INSERT INTO controladoria.icms_estabelecimentos (id_pessoa, "
                    + "descricao, nirf, incra, matricula, endereco, numero, "
                    + "bairro, municipio, inscri_est, total_area, atividades) "
                    + "VALUES ('" + idProd + "','" + desc + "','" + nirf + "',"
                    + "'" + incra + "','" + matricula + "','" + endereco + "',"
                    + "'" + numero + "','" + bairro + "','" + municipio + "',"
                    + "'" + inscrEst + "','" + area + "','" + atividades + "');";
        } else {
            sql = "UPDATE controladoria.icms_estabelecimentos "
                    + "SET descricao = '" + desc + "', nirf = '" + nirf + "',"
                    + "incra = '" + incra + "', matricula = '" + matricula + "',"
                    + "endereco = '" + endereco + "', numero = '" + numero + "',"
                    + "bairro = '" + bairro + "', total_area = '" + area + "',"
                    + "atividades = '" + atividades + "' "
                    + "WHERE id = '" + idArea + "' AND id_pessoa = '" + idProd + "';";
        }

        if (cn.conecta()) {
            try {
                if (cn.executeAtualizacao(sql)) {
                    limpaCampos();
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                cn.desconecta();
            }
            var_consulta = 99; //index 99 não existe. só irá pesquisar novamente a tabela.
            incluiPesquisa();
        }

    }

    private void carregaMunicipios() {
        sql = "SELECT * FROM controladoria.cad_munic a "
                + "WHERE a.uf = '" + jCbUF.getSelectedItem() + "' "
                + "ORDER BY a.nome;";

        jCbMunicipio.removeAllItems();

        if (cn.conecta()) {
            try {
                cn.executeConsulta(sql);
                while (cn.rs.next()) {
                    jCbMunicipio.addItem(cn.rs.getString("nome"));
                }
                
                jCbUF.setEnabled(false);
                jCbMunicipio.setEnabled(true);
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                cn.desconecta();
            }
        }
    }

