    private void incluiPesquisa() {

        switch (var_consulta) {
            case 1:
                int linha = jTblConsulta_Multi.getSelectedRow();

                jTxtIdProd.setText(jTblConsulta_Multi.getValueAt(linha, 0).toString());
                jTxtNomeProd.setText(jTblConsulta_Multi.getValueAt(linha, 1).toString());

                break;

            case 2:
                sql = "SELECT id, nome FROM controladoria.cad_pessoas "
                        + "WHERE id = '" + jTxtIdProd.getText() + "';";

                if (cn.conecta()) {
                    try {
                        cn.executeConsulta(sql);
                        while (cn.rs.next()) {
                            jTxtNomeProd.setText(cn.rs.getString("nome"));
                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    } finally {
                        cn.desconecta();
                    }
                }
        }

        if (!"".equals(jTxtNomeProd.getText())) {

            DefaultTableModel lista = montaTabela();

            sql = "SELECT * FROM controladoria.icms_estabelecimentos a "
                    + "WHERE a.id_pessoa = '" + jTxtIdProd.getText() + "';";

            if (cn.conecta()) {
                try {
                    cn.executeConsulta(sql);
                    while (cn.rs.next()) {
                        lista.addRow(new String[]{
                            cn.rs.getString("id"),
                            cn.rs.getString("descricao"),
                            cn.rs.getString("nirf"),
                            cn.rs.getString("incra"),
                            cn.rs.getString("matricula"),
                            cn.rs.getString("endereco"),
                            cn.rs.getString("bairro"),
                            cn.rs.getString("municipio"),
                            cn.rs.getString("inscri_est"),
                            df.format(cn.rs.getDouble("total_area")),
                            cn.rs.getString("atividades"),
                            cn.rs.getString("numero")
                        });

                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                } finally {
                    cn.desconecta();
                }
            }

        }

    }

