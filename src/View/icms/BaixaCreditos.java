    private void montaLista() {
        DefaultTableModel lista = (DefaultTableModel) jTblConsulta_Multi.getModel();
        lista.setColumnCount(0);
        lista.setRowCount(0);

        String sql;
        jTxtPesquisa_Multi.grabFocus();

        switch (var_consulta) {

            case 1:
                sql = "SELECT * FROM controladoria.rel_credito_disp "
                        + "WHERE credito_liberado > 0.00 "
                        + "ORDER BY nome,processo ;";

                lista.addColumn("Id");
                lista.addColumn("Nome");
                lista.addColumn("I.E.");
                lista.addColumn("Processo");

                jTblConsulta_Multi.getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getColumnModel().getColumn(0).setMinWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMinWidth(60);
                jTblConsulta_Multi.getColumnModel().getColumn(2).setMaxWidth(120);
                jTblConsulta_Multi.getColumnModel().getColumn(2).setMinWidth(120);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(2).setMaxWidth(120);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(2).setMinWidth(120);
                jTblConsulta_Multi.getColumnModel().getColumn(3).setMaxWidth(120);
                jTblConsulta_Multi.getColumnModel().getColumn(3).setMinWidth(120);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(3).setMaxWidth(120);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(3).setMinWidth(120);

                if (cn.conecta()) {
                    try {
                        cn.executeConsulta(sql);
                        while (cn.rs.next()) {
                            lista.addRow(new String[]{
                                cn.rs.getString("id"),
                                cn.rs.getString("nome"),
                                cn.rs.getString("inscri_est"),
                                cn.rs.getString("processo")

                            });

                        }

                    } catch (Exception e) {

                    } finally {
                        cn.desconecta();
                    }

                }

                break;

            case 2:

                sql = "SELECT * FROM controladoria.produtor_baixa_credito WHERE id = '" + jTxtId.getText() + "';";
                lista.addColumn("Ordem de transferência");
                lista.addColumn("Data");
                lista.addColumn("Valor");
                lista.addColumn("Nota Fiscal");

                if (cn.conecta()) {
                    try {
                        cn.executeConsulta(sql);
                        while (cn.rs.next()) {
                            lista.addRow(new String[]{
                                cn.rs.getString("otc"),
                                dateIn.format(cn.rs.getDate("dt_baixa")),
                                df.format(cn.rs.getDouble("valor")),
                                cn.rs.getString("nota_fiscal")

                            });
                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    }
                }
                break;
        }
    }

    private void incluiPesquisa() {

        int linha = jTblConsulta_Multi.getSelectedRow();

        switch (var_consulta) {
            case 1:
                jTxtId.setText(jTblConsulta_Multi.getValueAt(linha, 0).toString());
                jTxtNomeProdutor.setText(jTblConsulta_Multi.getValueAt(linha, 1).toString());

                calculaSaldos();

                break;

            case 2:

                jTxtOtc.setText(jTblConsulta_Multi.getValueAt(linha, 0).toString());
                jTxtData.setText(jTblConsulta_Multi.getValueAt(linha, 1).toString());
                jTxtValor.setText(jTblConsulta_Multi.getValueAt(linha, 2).toString());
                jTxtNf.setText(jTblConsulta_Multi.getValueAt(linha, 3).toString());

                break;
        }

    }

    /**
     * Define Resumos do lançamento.
     */
    private void calculaSaldos() {

        try {

            resumo = resumoDAO.buscar(Integer.parseInt(jTxtId.getText()));
            jTxtOrdens.setText(resumo.getQtd() + "");
            jTxtValorAprovado.setText(df.format(resumo.getCreditoLiberado()));
            jTxtValorRecebido.setText(df.format(resumo.getCreditoRecebido()));
            jTxtSaldo.setText((df.format(resumo.getCreditoLiberado() - resumo.getCreditoRecebido())));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi possível recuperar o resumo.\n" + e);
        }

    }

