    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (!"".equals(jTxtOrdens.getText())) {

            try {
                pk.setId(Integer.parseInt(jTxtId.getText()));
                pk.setOtc(Long.parseLong(jTxtOtc.getText()));
                bx = dao.deletar(pk);

                limpaCampos();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Não foi possível excluir o lançamento.\n" + e, "Exclusão de Lançamentos", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Você precisa selecionar um lançamento para Excluir.", "Exclusão de Lançamentos", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

